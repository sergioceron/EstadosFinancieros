    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /*String ne = JOptionPane.showInputDialog("Nombre de la empresa:");*/
        empresa = finanzasPUEntityManager.merge(empresa);
        Balance b = new Balance();
        b.setEmpresa(empresa);
        b.setFecha(new Date());
        finanzasPUEntityManager.getTransaction().begin();
        finanzasPUEntityManager.persist(b);
        empresa.getBalances().add(b);
        finanzasPUEntityManager.persist(empresa);
        finanzasPUEntityManager.getTransaction().commit();

        EsquemaMayorMain esq = new EsquemaMayorMain();
        esq.setBalance(b);
        esq.init();
        esq.setVisible(true);

        this.setVisible(false);
}//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Query q = finanzasPUEntityManager.createQuery("select b from Balance b where b.fecha=:fecha");
            q.setParameter("fecha", DateFormat.getDateInstance().parse(bal));
            Balance b = (Balance) q.getResultList().get(0);
            System.out.println(b);
            if (b != null) {
                EsquemaMayorMain esq = new EsquemaMayorMain();
                esq.setBalance(b);
                esq.init();
                esq.setVisible(true);
                this.setVisible(false);
            }
        } catch (ParseException ex) {
            Logger.getLogger(balances.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_jButton2ActionPerformed

