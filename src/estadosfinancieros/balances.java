    class BalanceConverter extends Converter {
        @Override
        public Object convertForward(Object arg) {
            Vector<String>  v = new Vector<String>();
            for( Object a : ((Set<Balance>)arg) ){
                System.out.println(a);
                Date fecha = ((Balance)a).getFecha();
                String f = DateFormat.getDateInstance().format(fecha);

                v.add(f);
            }

            return v;
        }

        @Override
        public Object convertReverse(Object arg) {
            return arg;
        }
    }

