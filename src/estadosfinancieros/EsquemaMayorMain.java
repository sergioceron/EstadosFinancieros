        sb.append("<p>&nbsp;</p>");
        sb.append("<table width='100%' style='font-family:Sans-Serif;font-size:10px;'>");
        sb.append("<tr><td><u>Origen</u></td><td><u>Aplicacion</u></td></tr>");
